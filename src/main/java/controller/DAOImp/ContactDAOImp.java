package controller.DAOImp;

import controller.DAO.ContactDAO;
import java.util.List;
import model.Contact;
import model.Role;
import model.Salary;
import org.hibernate.Session;
import org.hibernate.SessionFactory;
import org.hibernate.Transaction;
import org.hibernate.query.Query;
import util.HibernateUtil;

/**
 *
 * @author LENOVO
 */
public class ContactDAOImp implements ContactDAO {

    private Session session;

    public ContactDAOImp(Session session) {
        this.session = session;
    }

    @Override
    public boolean add(Contact t) {
        Transaction transaction = session.beginTransaction();
        try {
            session.saveOrUpdate(t); // Hibernate tự động quyết định là save hay update
            transaction.commit();
            return true;
        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback(); // Rollback nếu có lỗi xảy ra
            }
            e.printStackTrace();
            return false;
        }
    }

    @Override
    public Contact get(long id) {
        return session.get(Contact.class, id);
    }

    @Override
    public boolean update(Contact t) {
        Transaction transaction = session.beginTransaction();
        try {
            session.update(t);
            transaction.commit();
            return true;
        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback(); // Rollback nếu có lỗi
            }
            e.printStackTrace();
            return false;
        }
    }

    @Override
    public boolean delete(long id) {
        Transaction transaction = session.beginTransaction();
        try {
            Contact contact = session.find(Contact.class, id);
            session.delete(contact); // Xóa role
            transaction.commit();
        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback(); // Rollback nếu có lỗi
            }
            e.printStackTrace();
        }
        return true;
    }

    @Override
    public List<Contact> getAll() {
        Query<Contact> query = session.createQuery("FROM Contact", Contact.class);
        return query.list();
    }
}
